POLL_INTERVAL = 0.01
DEBOUNCE_INTERVAL = 0.03
TEMPO  = 0.065
TEMPO2 = 0.075


def button1_pressed?
  PLAY_BUTTON.read == 0
end

def button2_pressed?
  PLAY_BUTTON2.read == 0
end

def wait_for_button_press
  loop do
    if button1_pressed?
      sleep DEBOUNCE_INTERVAL
      return 1 if button1_pressed?
    end

    if button2_pressed?
      sleep DEBOUNCE_INTERVAL
      return 2 if button2_pressed?
    end

    sleep POLL_INTERVAL
  end
end

def wait_for_button_release
  sleep POLL_INTERVAL while button1_pressed? || button2_pressed?
end

def build_tracks(scores, tempo)
  scores.map do |score|
    pwm = PWM.new(score[:pin], duty: 75)

    {
      pwm: pwm,
      frequencies: score[:notes].map { |item| NOTES[item[0]] },
      durations: score[:notes].map { |item| item[1] * tempo },
      index: 0,
      next_time: Time.now,
      release_time: nil,
    }
  end
end

def play_tracks(tracks)
  loop do
    now = Time.now
    active_tracks = false

    tracks.each do |track|
      next if track[:finished]

      active_tracks = true

      if track[:release_time] && now >= track[:release_time]
        track[:pwm].pulse_width_us(0)
        track[:release_time] = nil
      end

      next if now < track[:next_time]

      if track[:index] >= track[:frequencies].length
        track[:finished] = true
        track[:pwm].pulse_width_us(0)
        next
      end

      frequency = track[:frequencies][track[:index]]
      duration = track[:durations][track[:index]]
      track[:index] += 1
      track[:next_time] = now + duration

      if frequency && frequency > 0
        period = (1_000_000.0 / frequency).to_i
        track[:pwm].period_us(period)
        track[:pwm].pulse_width_us(period / 2)
        track[:release_time] = now + duration * 0.9
      else
        track[:pwm].pulse_width_us(0)
      end
    end

    break unless active_tracks
  end
end

loop do
  which = wait_for_button_press
  scores = which == 1 ? SCORES  : SCORES2
  tempo  = which == 1 ? TEMPO   : TEMPO2
  play_tracks(build_tracks(scores, tempo))
  wait_for_button_release
end
