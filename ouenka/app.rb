BUTTON_PIN  = 10
BUTTON_PIN2 = 16
PLAY_BUTTON  = GPIO.new(BUTTON_PIN,  GPIO::IN | GPIO::PULL_UP)
PLAY_BUTTON2 = GPIO.new(BUTTON_PIN2, GPIO::IN | GPIO::PULL_UP)

SCORES = [
  { pin: 1, notes: pin0_score },
  { pin: 2, notes: pin1_score },
